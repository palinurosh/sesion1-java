

public class TiposDeDatos {
    public static void main(String[] args) {
        //tipos de datos:

        //1 numericos enteros
        byte variable = 4;
        short variable1 = 10;
        int variable2 = 23;
        long variable3 = 21;

        //1.2 numéricos decimales
        float variable4 = 20.2f; //sufijo f indica float. Si da error (porque por defecto lo toma como double) sin f ponerla.
        double variable5 = 20.4d; //sufijo d lo mismo que la anterior.

        //2 booleano (true o false)
        boolean variable6 = false;
        boolean variable7 = true;

        //3 texto
        char variable8 = 'a';
        String variable9 = "Esto es un string";

        //Ninguno de estos tipos de datos pueden tener valor nulo.
        //Los tipos de datos más comunes son:
        //int, long, double, booblean y String

    }
}
